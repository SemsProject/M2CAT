package de.unirostock.sems.M2CAT.connector.pmr2;

public class Pmr2Exception extends Exception {

	private static final long serialVersionUID = -6568233745659760216L;

	public Pmr2Exception() {
		super();
	}

	public Pmr2Exception(String message, Throwable cause) {
		super(message, cause);
	}

	public Pmr2Exception(String message) {
		super(message);
	}

	public Pmr2Exception(Throwable cause) {
		super(cause);
	}
	
	
}
